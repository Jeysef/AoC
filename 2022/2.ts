const rockPaperScissors2 = (opponentChar: string, meChar: string): number => {
    // 0: Rock, 1: Paper, 2: Scissors
    const opponent = opponentChar.charCodeAt(0) - 65;
    // 0: Lose, 1: Draw, 2: Win
    const result = meChar.charCodeAt(0) - 88;
    const strategy = [ // to make result 
        [2, 0, 1],
		[0, 1, 2],
		[1, 2, 0],
    ][result][opponent];
    /*
    ex: opponent: Rock, result: loose strategy[0][0] -> you need to play Scissors so 2
    ex: opponent: Paper, result: draw strategy[1][1] -> you need to play Paper so 1
    ex: opponent: Scissors, result: win strategy[2][2] -> you need to play Rock so 0
    */
    let score = 0
    score += [0,3,6][result]
    score += [1, 2, 3][strategy];
    return score;
}

